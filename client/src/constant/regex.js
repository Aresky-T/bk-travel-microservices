export const CUSTOM_REGEX = {
    EMAIL: /^[\w-.]+@([\w-]+\.)+[\w-]{2,4}$/,
    USERNAME: /^[A-Za-z0-9_-]{5,30}$/,
    USERNAME2: /^\S+$/,
    PASSWORD: /^(?=.*\d)(?=.*[a-z])(?=.*[A-Z])(?=.*[^a-zA-Z0-9])(?!.*\s).{8,20}$/,
    PHONE: /^[+]?[(]?[0-9]{3}[)]?[-\s.]?[0-9]{3}[-\s.]?[0-9]{4,6}$/,
    REGEX_STRING: /^(?!^\s+$)\s*\S.*\S\s*$/,
    REGEX_DATE: /^(\d{4}-\d{2}-\d{2})|(\d{2}\/\d{2}\/\d{4})|(\d{2}\/\d{2}\/\d{2})$/
}